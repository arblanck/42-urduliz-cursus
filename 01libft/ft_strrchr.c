#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	len;

	len = ft_strlen(str);
	while (len >= 0)
	{
		if ((char)c == str[len])
			return ((char *)&str[len]);
		len--;
	}
	return (0);
}
